#Store the principal amount, rate of interest, and time in different variables and then calculate the Simple Interest for 3 years. 
# Formula: Simple Interest = P x R x T / 100

#create a variable and store the values.
principal_amount = 2000                     #The Principal Amount(P)
rate_of_interest = 5                        #Rate of Interest(R)
time = 3                                    #Time(T)

#calculate the Simple Interest
SI = (principal_amount * rate_of_interest * time) / 100

#print the total simple interest
print("Principal Amount :", principal_amount)
print("Rate of interest :", rate_of_interest)
print("Time :", time ,"years")
print("Simple Interest :",SI)